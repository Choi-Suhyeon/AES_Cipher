#include "aes_enc.h"
#include "debug.h"

int main() {
    State s = { .bytes = {
        0x00, 0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77,
        0x88, 0x99, 0xAA, 0xBB, 0xCC, 0xDD, 0xEE, 0xFF,
    } };

    MasterKey mk = {
        .sz      = SZ_128,
        .k.sz128 = {
            0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF,
            0x12, 0x34, 0x56, 0x78, 0x9A, 0xBC, 0xDE, 0xF0,
        }
    };

    enc_with_aes(&s, &mk);
    debug_state("result", &s);

    return 0;
}
